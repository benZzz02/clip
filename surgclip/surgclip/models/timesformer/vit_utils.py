def drop_path(x, drop_prob: float = 0., training: bool = False):
    """Drop paths (Stochastic Depth) per sample (when applied in main path of residual blocks).
    This is the same as the DropConnect impl I created for EfficientNet, etc networks, however,
    the original name is misleading as 'Drop Connect' is a different form of dropout in a separate paper...
    See discussion: https://github.com/tensorflow/tpu/issues/494#issuecomment-532968956 ... I've opted for
    changing the layer and argument names to 'drop path' rather than mix DropConnect as a layer name and use
    'survival rate' as the argument.
    """
    if drop_prob == 0. or not training:
        return x
    keep_prob = 1 - drop_prob
    shape = (x.shape[0],) + (1,) * (x.ndim - 1)  # work with diff dim tensors, not just 2D ConvNets
    random_tensor = keep_prob + torch.rand(shape, dtype=x.dtype, device=x.device)
    random_tensor.floor_()  # binarize
    output = x.div(keep_prob) * random_tensor
    return output

class DropPath(nn.Module):
    """Drop paths (Stochastic Depth) per sample  (when applied in main path of residual blocks).
    """
    def __init__(self, drop_prob=None):
        super(DropPath, self).__init__()
        self.drop_prob = drop_prob

    def forward(self, x):
        return drop_path(x, self.drop_prob, self.training)

def hf_weight_translation(state_dict):
    new_state_dict = {}
    for key, value in state_dict.items():
        new_key = None
        
        # Handle embeddings
        if key == 'vit.embeddings.cls_token':
            new_key = 'cls_token'
        elif key == 'vit.embeddings.position_embeddings':
            new_key = 'pos_embed'
        elif key.startswith('vit.embeddings.patch_embeddings.projection'):
            new_key = key.replace('vit.embeddings.patch_embeddings.projection', 'patch_embed.proj')
        
        # Handle encoder layers
        elif key.startswith('vit.encoder.layer.'):
            # Extract layer number
            parts = key.split('.')
            layer_num = parts[3]  # vit.encoder.layer.{N}
            
            # Handle attention weights - need to combine q,k,v into qkv
            if 'attention.attention.query' in key:
                # Store for later combination
                q_key = f'blocks.{layer_num}.attn.qkv.weight' if 'weight' in key else f'blocks.{layer_num}.attn.qkv.bias'
                if q_key not in new_state_dict:
                    new_state_dict[q_key] = {'q': None, 'k': None, 'v': None}
                new_state_dict[q_key]['q'] = value
                continue
            elif 'attention.attention.key' in key:
                q_key = f'blocks.{layer_num}.attn.qkv.weight' if 'weight' in key else f'blocks.{layer_num}.attn.qkv.bias'
                if q_key not in new_state_dict:
                    new_state_dict[q_key] = {'q': None, 'k': None, 'v': None}
                new_state_dict[q_key]['k'] = value
                continue
            elif 'attention.attention.value' in key:
                q_key = f'blocks.{layer_num}.attn.qkv.weight' if 'weight' in key else f'blocks.{layer_num}.attn.qkv.bias'
                if q_key not in new_state_dict:
                    new_state_dict[q_key] = {'q': None, 'k': None, 'v': None}
                new_state_dict[q_key]['v'] = value
                continue
            elif 'attention.output.dense' in key:
                new_key = key.replace('vit.encoder.layer.', 'blocks.').replace('.attention.output.dense', '.attn.proj')
            
            # Handle layer norms
            elif 'layernorm_before' in key:
                new_key = key.replace('vit.encoder.layer.', 'blocks.').replace('.layernorm_before', '.norm1')
            elif 'layernorm_after' in key:
                new_key = key.replace('vit.encoder.layer.', 'blocks.').replace('.layernorm_after', '.norm2')
            
            # Handle MLP layers
            elif 'intermediate.dense' in key:
                new_key = key.replace('vit.encoder.layer.', 'blocks.').replace('.intermediate.dense', '.mlp.fc1')
            elif 'output.dense' in key and 'attention' not in key:
                new_key = key.replace('vit.encoder.layer.', 'blocks.').replace('.output.dense', '.mlp.fc2')
        
        # Handle final layer norm
        elif key == 'vit.layernorm.weight':
            new_key = 'norm.weight'
        elif key == 'vit.layernorm.bias':
            new_key = 'norm.bias'
        
        # Skip classifier layers as they're typically not needed for video models
        elif key.startswith('classifier'):
            continue
            
        # Add the mapped key-value pair
        if new_key is not None:
            new_state_dict[new_key] = value
    
    final_state_dict = {}
    
    for key, value in new_state_dict.items():
        if isinstance(value, dict) and 'q' in value:
            # This is a qkv tensor that needs to be combined
            if value['q'] is not None and value['k'] is not None and value['v'] is not None:
                # Concatenate along the first dimension (output dimension)
                combined = torch.cat([value['q'], value['k'], value['v']], dim=0)
                final_state_dict[key] = combined
            else:
                print(f"Warning: Incomplete qkv tensor for {key}")
        else:
            final_state_dict[key] = value
    
    return final_state_dict
    

